#include "../header/libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int	i;

	i = 0;
	if (s == NULL)
		return ;
	(void)fd;
	while (s[i] != '\0')
	{
		write(2, &s[i], 1);
		i++;
	}
	write(2, "\n", 1);
}
