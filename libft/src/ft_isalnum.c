#include "../header/libft.h"

int	ft_isalnum(int c)
{
	int	result;

	result = 0;
	if ((c >= '0' && c <= '9') || (c >= 'a' && c <= 'z')
		|| (c >= 'A' && c <= 'Z'))
		result = 1;
	return (result);
}
